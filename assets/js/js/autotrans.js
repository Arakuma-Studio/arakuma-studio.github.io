let lang = 'es';
function Init()
{
    const usrlang = navigator.language.substring(0, 2) || navigator.userLanguage.substring(0, 2);
    if (usrlang !== `${lang}`)
    {
      lang = 'en';
    }
    
    document.documentElement.setAttribute('lang', lang);
    
    let spanishButton = document.querySelector("[translatebutton='es']");
    let englishButton = document.querySelector("[translatebutton='en']");
    
    if (spanishButton !== null)
    { 
        spanishButton.onclick = function()
        { 
            lang = 'es';
            Translate('es');
            document.documentElement.setAttribute('lang', 'es');
        };
    }
    if (englishButton !== null)
    {
        document.documentElement.setAttribute('lang', lang);
        englishButton.onclick = function()
        {
            lang = 'en';
            Translate('en');
            document.documentElement.setAttribute('lang', 'en');
        }; 
    }
        
    Translate(lang);
}

function Translate(language)
{
    let keys = Object.keys(translations);
    
    for (let i = 0; i < keys.length; i++) 
    {
        let matches = document.querySelectorAll("[translate='" + keys[i] + "']");
        
        for (let j = 0; j < matches.length; j++)
        {
            matches[j].innerText = translations[keys[i]][language];
        }
    }
}

Init();
